"use client";

import { useState, useEffect } from "react";
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Badge } from "@/components/ui/badge";
import { Switch } from "@/components/ui/switch";
import { Label } from "@/components/ui/label";
import {
  Plus,
  Edit,
  Trash2,
  Save,
  X,
  RefreshCw,
  AlertTriangle,
  CheckCircle,
} from "lucide-react";
import { WorkType, workTypeStorage, DEFAULT_WORK_TYPES } from "@/lib/workTypes";
import { workTypeEpisodeMappingStorage } from "@/lib/workTypeEpisodeMapping";
import { customEpisodeTypeStorage } from "@/lib/customEpisodeTypes";
import {
  EpisodeType,
  DEFAULT_WORK_TYPE_EPISODE_MAPPING,
  CustomEpisodeType,
} from "@/types";

interface WorkTypeManagerProps {
  onTypesChange?: (types: WorkType[]) => void;
}

export default function WorkTypeManager({
  onTypesChange,
}: WorkTypeManagerProps) {
  const [workTypes, setWorkTypes] = useState<WorkType[]>([]);
  const [editingType, setEditingType] = useState<WorkType | null>(null);
  const [isAdding, setIsAdding] = useState(false);
  const [editingEpisodeMapping, setEditingEpisodeMapping] = useState<{
    episodeTypes: EpisodeType[];
    defaultEpisodeType: EpisodeType;
  } | null>(null);
  const [availableEpisodeTypes, setAvailableEpisodeTypes] = useState<
    CustomEpisodeType[]
  >([]);
  const [message, setMessage] = useState<{
    type: "success" | "error";
    text: string;
  } | null>(null);

  useEffect(() => {
    loadWorkTypes();
    loadEpisodeTypes();
  }, []);

  const loadWorkTypes = () => {
    try {
      const types = workTypeStorage.getAll();
      setWorkTypes(types);
      onTypesChange?.(types);
    } catch (error) {
      console.error("ËºâÂÖ•‰ΩúÂìÅÈ°ûÂûãÂ§±Êïó:", error);
      setWorkTypes(DEFAULT_WORK_TYPES);
    }
  };

  const loadEpisodeTypes = () => {
    try {
      const types = customEpisodeTypeStorage.getEnabledTypes();
      setAvailableEpisodeTypes(types);
    } catch (error) {
      console.error("ËºâÂÖ•ÈõÜÊï∏È°ûÂûãÂ§±Êïó:", error);
    }
  };

  const showMessage = (type: "success" | "error", text: string) => {
    setMessage({ type, text });
    setTimeout(() => setMessage(null), 3000);
  };

  const handleAddType = () => {
    setIsAdding(true);
    setEditingType({
      id: "",
      name: "",
      color: "#3B82F6",
      icon: "üìù",
      description: "",
      isDefault: false,
      isEnabled: true,
      createdAt: new Date().toISOString(),
    });

    // Ë®≠ÂÆöÈ†êË®≠ÈõÜÊï∏È°ûÂûã
    const enabledTypes = customEpisodeTypeStorage.getEnabledTypes();
    if (enabledTypes.length > 0) {
      setEditingEpisodeMapping({
        episodeTypes: [enabledTypes[0].name],
        defaultEpisodeType: enabledTypes[0].name,
      });
    }
  };

  const handleEditType = (type: WorkType) => {
    setEditingType({ ...type });
    setIsAdding(false);

    // ËºâÂÖ•Â∞çÊáâÁöÑÈõÜÊï∏È°ûÂûãË®≠ÂÆö
    try {
      const mapping = workTypeEpisodeMappingStorage.getByWorkType(type.name);
      if (mapping) {
        setEditingEpisodeMapping({
          episodeTypes: mapping.episodeTypes,
          defaultEpisodeType: mapping.defaultEpisodeType,
        });
      } else {
        // ‰ΩøÁî®È†êË®≠Ë®≠ÂÆö
        const defaultMapping = DEFAULT_WORK_TYPE_EPISODE_MAPPING.find(
          (m) => m.workType === type.name
        );
        setEditingEpisodeMapping({
          episodeTypes: defaultMapping?.episodeTypes || ["episode"],
          defaultEpisodeType: defaultMapping?.defaultEpisodeType || "episode",
        });
      }
    } catch (error) {
      console.error("ËºâÂÖ•ÈõÜÊï∏È°ûÂûãË®≠ÂÆöÂ§±Êïó:", error);
      // ‰ΩøÁî®È†êË®≠Ë®≠ÂÆö
      setEditingEpisodeMapping({
        episodeTypes: ["episode"],
        defaultEpisodeType: "episode",
      });
    }
  };

  const handleSaveType = () => {
    if (!editingType || !editingEpisodeMapping) return;

    try {
      // È©óË≠âËº∏ÂÖ•
      if (!editingType.name.trim()) {
        showMessage("error", "Ë´ãËº∏ÂÖ•È°ûÂûãÂêçÁ®±");
        return;
      }

      if (!editingType.color) {
        showMessage("error", "Ë´ãÈÅ∏ÊìáÈ°èËâ≤");
        return;
      }

      // È©óË≠âÈõÜÊï∏È°ûÂûãË®≠ÂÆö
      if (editingEpisodeMapping.episodeTypes.length === 0) {
        showMessage("error", "Ë´ãËá≥Â∞ëÈÅ∏Êìá‰∏ÄÂÄãÈõÜÊï∏È°ûÂûã");
        return;
      }

      if (
        !editingEpisodeMapping.episodeTypes.includes(
          editingEpisodeMapping.defaultEpisodeType
        )
      ) {
        showMessage("error", "È†êË®≠ÈõÜÊï∏È°ûÂûãÂøÖÈ†àÂú®ÂèØÁî®ÁöÑÈõÜÊï∏È°ûÂûã‰∏≠");
        return;
      }

      // Ê™¢Êü•ÂêçÁ®±ÈáçË§á
      if (workTypeStorage.isNameDuplicate(editingType.name, editingType.id)) {
        showMessage("error", "È°ûÂûãÂêçÁ®±Â∑≤Â≠òÂú®");
        return;
      }

      if (isAdding) {
        // Êñ∞Â¢ûÈ°ûÂûã
        const newType = workTypeStorage.create({
          name: editingType.name.trim(),
          color: editingType.color,
          icon: editingType.icon || "üìù",
          description: editingType.description?.trim() || "",
          isDefault: false,
          isEnabled: editingType.isEnabled,
        });

        // Êñ∞Â¢ûÈõÜÊï∏È°ûÂûãÂ∞çÊáâ
        try {
          workTypeEpisodeMappingStorage.create({
            workType: newType.name,
            episodeTypes: editingEpisodeMapping.episodeTypes,
            defaultEpisodeType: editingEpisodeMapping.defaultEpisodeType,
          });
        } catch (error) {
          console.error("Êñ∞Â¢ûÈõÜÊï∏È°ûÂûãÂ∞çÊáâÂ§±Êïó:", error);
        }

        showMessage("success", `ÊàêÂäüÊñ∞Â¢ûÈ°ûÂûã„Äå${newType.name}„Äç`);
      } else {
        // Êõ¥Êñ∞È°ûÂûã
        if (!editingType.id) return;

        const oldName = workTypeStorage
          .getAll()
          .find((t) => t.id === editingType.id)?.name;
        const updatedType = workTypeStorage.update(editingType.id, {
          name: editingType.name.trim(),
          color: editingType.color,
          icon: editingType.icon || "üìù",
          description: editingType.description?.trim() || "",
          isEnabled: editingType.isEnabled,
        });

        if (updatedType) {
          // Êõ¥Êñ∞ÈõÜÊï∏È°ûÂûãÂ∞çÊáâ
          try {
            if (oldName && oldName !== updatedType.name) {
              // Â¶ÇÊûúÂêçÁ®±ÊîπËÆäÔºåÂà™Èô§ËàäÁöÑÂ∞çÊáâÈóú‰øÇ
              workTypeEpisodeMappingStorage.delete(oldName);
            }

            // Êñ∞Â¢ûÊàñÊõ¥Êñ∞Â∞çÊáâÈóú‰øÇ
            const existingMapping = workTypeEpisodeMappingStorage.getByWorkType(
              updatedType.name
            );
            if (existingMapping) {
              workTypeEpisodeMappingStorage.update(updatedType.name, {
                episodeTypes: editingEpisodeMapping.episodeTypes,
                defaultEpisodeType: editingEpisodeMapping.defaultEpisodeType,
              });
            } else {
              workTypeEpisodeMappingStorage.create({
                workType: updatedType.name,
                episodeTypes: editingEpisodeMapping.episodeTypes,
                defaultEpisodeType: editingEpisodeMapping.defaultEpisodeType,
              });
            }
          } catch (error) {
            console.error("Êõ¥Êñ∞ÈõÜÊï∏È°ûÂûãÂ∞çÊáâÂ§±Êïó:", error);
          }

          showMessage("success", `ÊàêÂäüÊõ¥Êñ∞È°ûÂûã„Äå${updatedType.name}„Äç`);
        } else {
          showMessage("error", "Êõ¥Êñ∞Â§±Êïó");
        }
      }

      setEditingType(null);
      setEditingEpisodeMapping(null);
      setIsAdding(false);
      loadWorkTypes();
    } catch (error) {
      console.error("ÂÑ≤Â≠ò‰ΩúÂìÅÈ°ûÂûãÂ§±Êïó:", error);
      showMessage("error", error instanceof Error ? error.message : "ÂÑ≤Â≠òÂ§±Êïó");
    }
  };

  const handleDeleteType = (type: WorkType) => {
    if (type.isDefault) {
      showMessage("error", "ÁÑ°Ê≥ïÂà™Èô§È†êË®≠ÁöÑ‰ΩúÂìÅÈ°ûÂûã");
      return;
    }

    if (confirm(`Á¢∫ÂÆöË¶ÅÂà™Èô§È°ûÂûã„Äå${type.name}„ÄçÂóéÔºü`)) {
      try {
        workTypeStorage.delete(type.id);

        // ÂêåÊôÇÂà™Èô§ÈõÜÊï∏È°ûÂûãÂ∞çÊáâ
        try {
          workTypeEpisodeMappingStorage.delete(type.name);
        } catch (error) {
          console.error("Âà™Èô§ÈõÜÊï∏È°ûÂûãÂ∞çÊáâÂ§±Êïó:", error);
        }

        showMessage("success", `ÊàêÂäüÂà™Èô§È°ûÂûã„Äå${type.name}„Äç`);
        loadWorkTypes();
      } catch (error) {
        console.error("Âà™Èô§‰ΩúÂìÅÈ°ûÂûãÂ§±Êïó:", error);
        showMessage(
          "error",
          error instanceof Error ? error.message : "Âà™Èô§Â§±Êïó"
        );
      }
    }
  };

  const handleToggleEnabled = (type: WorkType) => {
    try {
      workTypeStorage.toggleEnabled(type.id);
      loadWorkTypes();
    } catch (error) {
      console.error("ÂàáÊèõÂïüÁî®ÁãÄÊÖãÂ§±Êïó:", error);
      showMessage("error", "ÂàáÊèõÂ§±Êïó");
    }
  };

  const handleResetToDefault = () => {
    if (
      confirm("Á¢∫ÂÆöË¶ÅÈáçÁΩÆÁÇ∫È†êË®≠È°ûÂûãÂóéÔºüÈÄôÂ∞áÊ∏ÖÈô§ÊâÄÊúâËá™Ë®ÇÈ°ûÂûãÂíåÈõÜÊï∏È°ûÂûãÂ∞çÊáâ„ÄÇ")
    ) {
      try {
        workTypeStorage.resetToDefault();

        // ÂêåÊôÇÈáçÁΩÆÈõÜÊï∏È°ûÂûãÂ∞çÊáâ
        try {
          workTypeEpisodeMappingStorage.resetToDefault();
        } catch (error) {
          console.error("ÈáçÁΩÆÈõÜÊï∏È°ûÂûãÂ∞çÊáâÂ§±Êïó:", error);
        }

        showMessage("success", "Â∑≤ÈáçÁΩÆÁÇ∫È†êË®≠È°ûÂûã");
        loadWorkTypes();
      } catch (error) {
        console.error("ÈáçÁΩÆ‰ΩúÂìÅÈ°ûÂûãÂ§±Êïó:", error);
        showMessage("error", "ÈáçÁΩÆÂ§±Êïó");
      }
    }
  };

  const handleCancel = () => {
    setEditingType(null);
    setEditingEpisodeMapping(null);
    setIsAdding(false);
  };

  return (
    <div className="space-y-4">
      {/* Ë®äÊÅØÈ°ØÁ§∫ */}
      {message && (
        <div
          className={`p-3 rounded-md flex items-center gap-2 ${
            message.type === "success"
              ? "bg-green-100 dark:bg-green-900/20 text-green-800 dark:text-green-200 border border-green-200 dark:border-green-800"
              : "bg-red-100 dark:bg-red-900/20 text-red-800 dark:text-red-200 border border-red-200 dark:border-red-800"
          }`}
        >
          {message.type === "success" ? (
            <CheckCircle className="w-4 h-4" />
          ) : (
            <AlertTriangle className="w-4 h-4" />
          )}
          {message.text}
        </div>
      )}

      {/* Á∑®ËºØË°®ÂñÆ */}
      {editingType && (
        <Card>
          <CardHeader>
            <CardTitle className="flex items-center gap-2">
              {isAdding ? (
                <>
                  <Plus className="w-5 h-5" />
                  Êñ∞Â¢û‰ΩúÂìÅÈ°ûÂûã
                </>
              ) : (
                <>
                  <Edit className="w-5 h-5" />
                  Á∑®ËºØ‰ΩúÂìÅÈ°ûÂûã
                </>
              )}
            </CardTitle>
          </CardHeader>
          <CardContent className="space-y-4">
            <div className="grid grid-cols-2 gap-4">
              <div>
                <Label className="text-sm font-medium">È°ûÂûãÂêçÁ®± *</Label>
                <Input
                  value={editingType.name}
                  onChange={(e) =>
                    setEditingType({ ...editingType, name: e.target.value })
                  }
                  placeholder="‰æãÂ¶ÇÔºöÂãïÁï´"
                  className="mt-1"
                />
              </div>
              <div>
                <Label className="text-sm font-medium">ÊèèËø∞</Label>
                <Input
                  value={editingType.description}
                  onChange={(e) =>
                    setEditingType({
                      ...editingType,
                      description: e.target.value,
                    })
                  }
                  placeholder="ÂèØÈÅ∏ÁöÑÊèèËø∞"
                  className="mt-1"
                />
              </div>
            </div>

            <div className="grid grid-cols-2 gap-4">
              <div>
                <Label className="text-sm font-medium">ÂúñÁ§∫</Label>
                <Input
                  value={editingType.icon}
                  onChange={(e) =>
                    setEditingType({ ...editingType, icon: e.target.value })
                  }
                  placeholder="‰æãÂ¶ÇÔºöüì∫"
                  className="mt-1"
                />
              </div>
              <div>
                <Label className="text-sm font-medium">È°èËâ≤</Label>
                <div className="flex items-center gap-2 mt-1">
                  <input
                    type="color"
                    value={editingType.color}
                    onChange={(e) =>
                      setEditingType({ ...editingType, color: e.target.value })
                    }
                    className="w-10 h-10 rounded border"
                  />
                  <span className="description-container">
                    {editingType.color}
                  </span>
                </div>
              </div>
            </div>

            <div className="flex items-center justify-between">
              <div>
                <Label className="text-sm font-medium">ÂïüÁî®</Label>
                <p className="text-xs-muted">ÊòØÂê¶ÂïüÁî®Ê≠§È°ûÂûã</p>
              </div>
              <Switch
                checked={editingType.isEnabled}
                onCheckedChange={(checked) =>
                  setEditingType({ ...editingType, isEnabled: checked })
                }
              />
            </div>

            {/* ÈõÜÊï∏È°ûÂûãË®≠ÂÆö */}
            {editingEpisodeMapping && (
              <div className="space-y-4 border-t pt-4">
                <div>
                  <Label className="text-sm font-medium">ÈõÜÊï∏È°ûÂûãË®≠ÂÆö</Label>
                  <p className="text-xs-muted">Ë®≠ÂÆöÊ≠§‰ΩúÂìÅÈ°ûÂûãÂèØÁî®ÁöÑÈõÜÊï∏È°ûÂûã</p>
                </div>

                <div>
                  <Label className="text-sm font-medium">
                    ÂèØÁî®ÁöÑÈõÜÊï∏È°ûÂûã *
                  </Label>
                  <div className="grid grid-cols-2 gap-2 mt-2">
                    {availableEpisodeTypes.map((type) => (
                      <div
                        key={type.id}
                        className="flex items-center space-x-2"
                      >
                        <Switch
                          checked={editingEpisodeMapping.episodeTypes.includes(
                            type.name
                          )}
                          onCheckedChange={() => {
                            const newEpisodeTypes =
                              editingEpisodeMapping.episodeTypes.includes(
                                type.name
                              )
                                ? editingEpisodeMapping.episodeTypes.filter(
                                    (t) => t !== type.name
                                  )
                                : [
                                    ...editingEpisodeMapping.episodeTypes,
                                    type.name,
                                  ];

                            setEditingEpisodeMapping({
                              ...editingEpisodeMapping,
                              episodeTypes: newEpisodeTypes,
                            });
                          }}
                        />
                        <div className="flex items-center gap-2">
                          <div className="w-4 h-4 rounded-full flex items-center justify-center text-white text-xs">
                            {type.icon}
                          </div>
                          <Label className="text-sm">{type.label}</Label>
                        </div>
                      </div>
                    ))}
                  </div>
                </div>

                <div>
                  <Label className="text-sm font-medium">È†êË®≠ÈõÜÊï∏È°ûÂûã *</Label>
                  <select
                    value={editingEpisodeMapping.defaultEpisodeType}
                    onChange={(e) =>
                      setEditingEpisodeMapping({
                        ...editingEpisodeMapping,
                        defaultEpisodeType: e.target.value,
                      })
                    }
                    className="w-full mt-1 p-2 border rounded-md dark:text-foreground/95 dark:bg-background/95"
                    required
                  >
                    {editingEpisodeMapping.episodeTypes.map((type) => {
                      const episodeType = availableEpisodeTypes.find(
                        (t) => t.name === type
                      );
                      return (
                        <option key={type} value={type}>
                          {episodeType?.label || type}
                        </option>
                      );
                    })}
                  </select>
                </div>
              </div>
            )}

            <div className="flex gap-2">
              <Button onClick={handleSaveType}>
                <Save className="w-4 h-4 mr-2" />
                ÂÑ≤Â≠ò
              </Button>
              <Button variant="outline" onClick={handleCancel}>
                ÂèñÊ∂à
              </Button>
            </div>
          </CardContent>
        </Card>
      )}

      {/* È°ûÂûãÂàóË°® */}
      <Card>
        <CardHeader>
          <div className="flex items-center justify-between">
            <CardTitle>‰ΩúÂìÅÈ°ûÂûã</CardTitle>
            <div className="flex gap-2">
              <Button onClick={handleAddType} variant="outline" size="sm">
                <Plus className="w-4 h-4 mr-2" />
                Êñ∞Â¢ûÈ°ûÂûã
              </Button>
              <Button
                onClick={handleResetToDefault}
                variant="outline"
                size="sm"
              >
                <RefreshCw className="w-4 h-4 mr-2" />
                ÈáçÁΩÆÈ†êË®≠
              </Button>
            </div>
          </div>
        </CardHeader>
        <CardContent>
          <div className="space-y-2">
            {workTypes.map((type) => (
              <div
                key={type.id}
                className="flex items-center justify-between p-3 border rounded-lg"
              >
                <div className="flex items-center gap-3">
                  <div className="w-8 h-8 rounded-full flex items-center justify-center text-white">
                    {type.icon}
                  </div>
                  <div>
                    <div className="font-medium">{type.name}</div>
                    {type.description && (
                      <div className="text-sm text-gray-500 dark:text-gray-400">
                        {type.description}
                      </div>
                    )}
                  </div>
                  {type.isDefault && (
                    <Badge variant="secondary" className="text-xs">
                      È†êË®≠
                    </Badge>
                  )}
                </div>
                <div className="flex items-center gap-2">
                  <Switch
                    checked={type.isEnabled}
                    onCheckedChange={() => handleToggleEnabled(type)}
                  />
                  <Button
                    onClick={() => handleEditType(type)}
                    variant="outline"
                    size="sm"
                  >
                    <Edit className="w-4 h-4" />
                  </Button>
                  {!type.isDefault && (
                    <Button
                      onClick={() => handleDeleteType(type)}
                      variant="outline"
                      size="sm"
                      className="text-red-600 dark:text-red-400 hover:text-red-700 dark:hover:text-red-300"
                    >
                      <Trash2 className="w-4 h-4" />
                    </Button>
                  )}
                </div>
              </div>
            ))}
          </div>
        </CardContent>
      </Card>
    </div>
  );
}
